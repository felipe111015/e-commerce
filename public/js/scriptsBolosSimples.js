


$('#enviarCarrinho1').click(function(){

    if($('#qtdeProds1').val() <= 0 || $('#qtdeProds1').val() === undefined){
        swal("Falha!!", "Informe a quantidade do produto!", "error");
       
    } else {
        swal("Êxito", "Produto Enviado ao Carrinho!", "success");
    }
  
 });

